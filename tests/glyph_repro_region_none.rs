fn flatten(contour: &[(f64, f64)]) -> Vec<f64> {
    let mut flat = Vec::with_capacity(contour.len() * 2);
    for &(x, y) in contour {
        flat.push(x);
        flat.push(y);
    }
    flat
}

#[test]
fn native_glyph_must_not_panic_nonzero() {
    assert_eq!(GLYPH_CONTOUR.len(), 184, "transcribed contour length");
    let flat = flatten(GLYPH_CONTOUR);
    let mut tess = Tessellator::new();
    tess.add_contour(2, &flat);
    // MUST NOT panic in `Sweep::region()` on a freed (`None`) region slot,
    // and must produce a real tessellation (the freed-region path used to
    // abort the whole sweep).
    let ok = tess.tessellate(WindingRule::NonZero, ElementType::Polygons, 3, 2, None);
    assert!(ok, "tessellation should succeed");
    assert!(
        !tess.elements().is_empty() && !tess.vertices().is_empty(),
        "expected non-empty triangulation: {} verts, {} elems",
        tess.vertices().len(),
        tess.elements().len()
    );
}

/// Same input, every winding rule, to confirm the fix isn't rule-specific.
#[test]
fn native_glyph_must_not_panic_all_rules() {
    let flat = flatten(GLYPH_CONTOUR);
    for rule in [
        WindingRule::Odd,
        WindingRule::NonZero,
        WindingRule::Positive,
        WindingRule::Negative,
        WindingRule::AbsGeqTwo,
    ] {
        let mut tess = Tessellator::new();
        tess.add_contour(2, &flat);
        let _ok = tess.tessellate(rule, ElementType::Polygons, 3, 2, None);
    }
}
